from math import pi

# Metric Prefixes ========================
femto = 1E-15
pico = femto*1000
nano = pico*1000
micro = nano*1000
milli = micro*1000
centi = 1E-2
kilo = 1E3
mega = kilo*1000
giga = mega*1000

# Angular Conversions =====================
deg2rad = pi/180
rad2deg = 180/pi
